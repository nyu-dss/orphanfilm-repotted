<h1>The 2012 Helen Hill Award</h1>
<h3>presented by Orphan Film Symposium partners</h3>
<p></p>
<p></p>
                        
<p>The University of South Carolina, the Nickelodeon Theatre of Columbia, South Carolina, and New York University's Department of Cinema Studies will announce the 2012 award recipient/s in fall 2011.</p>

<p>To honor the legacy of Helen Hill -- filmmaker, educator, animator, and native of Columbia, South Carolina -- the award supports independent filmmakers of exceptional talent. Recipients are funded to participate in the biennial Orphan Film Symposium. </p>

<p>New York University hosts the 8th Orphan Film Symposium, April 11-14, 2012, at New York's Museum of the Moving Image. There the recipient/s will screen work for an eclectic audience of artists, scholars, archivists, curators, collectors, distributors, and others.</p>

<p>Jurors will select filmmakers whose work affirms Helen Hill's artistic legacy, lived values, and everyday passions. Submitted films should celebrate and embody such things as creativity, self-expression, animation, small-gauge film, homemade movies and all things made by hand, collaboration, generosity, liberal spirituality, activism, love, play, community, and connection.</p>

<p>The deadline for applications to the 2012 award has passed.</p>

